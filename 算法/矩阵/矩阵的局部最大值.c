#include <stdio.h>

// 题目要求：
// 如果整数矩阵A 的非边界元素A[i][j]大于相邻的上、下、左、右4 个元素，
// 则对称元素A[i][j]是矩阵A 的局部最大值，
// 设计程序实现对任一输入的m 行n 列整数矩阵，输出该矩阵所有局部极大值及其所在的位置。

// 思路：
// 1. 遍历矩阵中的每一个元素，跳过边界元素。
// 2. 对于每一个元素，检查它的上、下、左、右四个元素。
// 3. 如果当前元素大于它的四个相邻元素，那么它就是局部最大值，输出它和它的位置。

int main()
{
    int m, n;
    printf("请输入矩阵的行数和列数：\n");
    scanf("%d %d", &m, &n); // 输入矩阵的行数和列数

    int A[m][n];
    printf("请输入矩阵的元素：\n");
    for (int i = 0; i < m; i++)
    { // 输入矩阵的元素
        for (int j = 0; j < n; j++)
        {
            scanf("%d", &A[i][j]);
        }
    }

    printf("局部最大值及其位置为：\n");
    for (int i = 1; i < m - 1; i++)
    { // 遍历矩阵中的每一个元素，跳过边界元素
        for (int j = 1; j < n - 1; j++)
        {
            // 如果当前元素大于它的四个相邻元素，那么它就是局部最大值，输出它和它的位置
            if (A[i][j] > A[i - 1][j] &&
                A[i][j] > A[i + 1][j] &&
                A[i][j] > A[i][j - 1] &&
                A[i][j] > A[i][j + 1])
            {
                printf("局部最大值：%d，位置：(%d, %d)\n", A[i][j], i, j);
            }
        }
    }

    return 0;
}

// 总结：
// 本题主要考察了二维数组的遍历和元素比较。
// 解决该问题的关键在于理解题目要求的“局部最大值”的定义，并能够正确地在二维数组中找到这些局部最大值。
// 在实现过程中，需要注意的是，由于题目要求的是非边界元素，因此在遍历矩阵时，需要跳过边界元素。
// 另外，由于需要比较一个元素和它的上、下、左、右四个元素，因此在实现比较逻辑时，需要确保不会越界。
