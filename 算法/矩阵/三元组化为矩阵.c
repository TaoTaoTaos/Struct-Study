#include <stdio.h>

// 题目要求：将一个使用三元组存储的稀疏矩阵恢复成原矩阵

// 思路和算法原理：
// 1. 三元组是一种稀疏矩阵的存储方式，每个非零元素由其行标、列标和值组成一个三元组。
// 2. 恢复矩阵就是根据三元组中的行标、列标和值在新的二维数组中填充对应的位置。

typedef struct
{
    int row;
    int col;
    int val;
} Tuple;

int main()
{
    // 定义一个5x5的稀疏矩阵的三元组表示
    Tuple a[5] = {
        {0, 0, 1},
        {1, 1, 2},
        {2, 2, 3},
        {3, 3, 4},
        {4, 4, 5}};

    // 定义一个5x5的矩阵用于存放恢复后的矩阵
    int b[5][5] = {0}; // 初始化为全零矩阵

    // 根据三元组恢复矩阵
    for (int i = 0; i < 5; i++)
    {                                     // 遍历每一个三元组
        b[a[i].row][a[i].col] = a[i].val; // 在对应的位置填充值
    }

    // 打印恢复后的矩阵
    for (int i = 0; i < 5; i++)
    {
        for (int j = 0; j < 5; j++)
        {
            printf("%d ", b[i][j]); // 打印每一个元素
        }
        printf("\n"); // 每打印完一行后换行
    }

    return 0;
}

// 总结：
// 本题考察了二维数组的使用、稀疏矩阵的三元组表示以及矩阵的恢复操作。通过遍历原矩阵的每一个三元组，将其值填充到新的二维数组的对应位置，从而实现了矩阵的恢复。
