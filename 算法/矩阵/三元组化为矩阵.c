//! 2. 如何进行矩阵的恢复操作？
//!    矩阵的恢复就是根据三元组中的行标、列标和值在新的二维数组中填充对应的位置。通过遍历原矩阵的每一个三元组，将其值填充到新的二维数组的对应位置，从而实现了矩阵的恢复。
