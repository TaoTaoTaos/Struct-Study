// 题目要求：实现顺序查找的算法

// 思路：
// 1. 从数组的第一个元素开始，逐个与目标值进行比较。
// 2. 如果找到了与目标值相等的元素，返回该元素的索引。
// 3. 如果遍历完整个数组都没有找到与目标值相等的元素，返回-1。

#include <stdio.h>

// 顺序查找函数，参数为目标数组和目标值
int sequentialSearch(int arr[], int len, int target) {
    // 遍历数组
    for (int i = 0; i < len; i++) {
        // 如果找到目标值，返回索引
        if (arr[i] == target) {
            return i;
        }
    }
    // 如果没有找到，返回-1
    return -1;
}

int main() {
    // 测试数组
    int arr[] = {1, 2, 3, 4, 5, 6, 7, 8, 9};
    // 数组长度
    int len = sizeof(arr) / sizeof(arr[0]);
    // 目标值
    int target = 5;

    // 调用顺序查找函数
    int index = sequentialSearch(arr, len, target);

    // 打印结果
    if (index != -1) {
        printf("找到目标值%d，索引为%d\n", target, index);
    } else {
        printf("未找到目标值%d\n", target);
    }

    return 0;
}

