// 本题考察的是顺序查找算法的实现。顺序查找是最基本的查找算法，它的基本思想是从数组的第一个元素开始，逐个与目标值进行比较，直到找到与目标值相等的元素或遍历完整个数组。
// 本题的关键是理解顺序查找的基本思想，并能够正确地在代码中实现这一思想。
// 1. 顺序查找的时间复杂度为O(n)，其中n为数组的长度。这是因为在最坏的情况下，可能需要遍历整个数组才能找到目标值或确定目标值不在数组中。
// 2. 顺序查找不要求数组是有序的，这是它与其他一些查找算法（如二分查找）的一个重要区别。
// 3. 在实现顺序查找的代码时，需要注意边界条件的处理，例如当目标值不在数组中时，应返回一个特殊的值（如-1）来表示查找失败。
