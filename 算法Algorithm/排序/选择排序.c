// 选择排序算法实现
#include <stdio.h>

// 选择排序函数
//!关键：找有没有比我小的，等到遍历完了，如果我是最小的，就把我换到最前面去

//选择排序是向后遍历整个数组
//插入排序是向前遍历
//他两都是把最小的放在前面

void selectionSort(int arr[], int n)
{
    int i, j, minIndex, tmp; // 定义循环变量，最小元素索引和临时变量
    for (i = 0; i < n - 1; i++) // 遍历每一个元素
    {                
        minIndex = i; //!先默认第一个是最小的
        for (j = i + 1; j < n; j++)
        { // 在剩余的元素中寻找有没有比他还小的
            if (arr[j] < arr[minIndex])
            {                 // 如果找到更小的元素
                minIndex = j; // 更新最小元素的索引 
            }
        }
        if (minIndex != i)
        {                 // 如果最小元素不是当前元素
            tmp = arr[i]; // 交换当前元素和最小元素
            arr[i] = arr[minIndex];
            arr[minIndex] = tmp;
        }
        //!完成一次循环时，一定会把最小的换到最前面，然后i++
    }
}

int main()
{
    int arr[] = {64, 25, 12, 22, 11};     // 定义一个数组
    int n = sizeof(arr) / sizeof(arr[0]); // 计算数组的长度
    selectionSort(arr, n);                // 对数组进行选择排序
    printf("排序后的数组是：\n");
    for (int i = 0; i < n; i++) // 输出排序后的数组
        printf("%d ", arr[i]);
    printf("\n");
    return 0;
}

/*这段代码实现了选择排序算法。选择排序是一种简单直观的排序算法，它的工作原理是每一次从待排序的数据元素中选出最小（或最大）的一个元素，存放在序列的起始位置，直到全部待排序的数据元素排完。这段代码中的选择排序函数就是按照这个原理实现的。在主函数中，我们定义了一个数组，然后调用选择排序函数对数组进行排序，最后输出排序后的数组。*/