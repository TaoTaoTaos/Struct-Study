// 题目要求：通过循环按行顺序为一个5*5的矩阵赋予1、3、5、7、9、11……等奇数，然后输出矩阵左下三角的值。
// 思路：首先，我们需要创建一个5*5的二维数组。然后，我们使用一个嵌套循环来遍历这个数组，按行顺序赋值。我们使用一个变量来存储当前的奇数值，每次赋值后，我们将这个变量加2。最后，我们再次遍历这个数组，打印出左下三角的值。

#include <stdio.h>

int main()
{
    // 创建一个5*5的二维数组
    int matrix[5][5];

    // 当前的奇数值
    int currentOddNumber = 1;

    // 使用一个嵌套循环来遍历数组，按行顺序赋值
    for (int i = 0; i < 5; i++)
    {
        for (int j = 0; j < 5; j++)
        {
            // 赋值
            matrix[i][j] = currentOddNumber;
            // 将当前的奇数值加2
            currentOddNumber += 2;
        }
    }

    // 再次遍历数组，打印出左下三角的值
    for (int i = 0; i < 5; i++)
    {
        for (int j = 0; j <= i; j++)
        {
            // 打印值
            printf("%d ", matrix[i][j]);
        }
        // 换行
        printf("\n");
    }

    return 0;
}

// 总结：这个题目主要考察了二维数组的使用，以及如何通过循环来遍历和赋值。同时，也考察了如何打印出二维数组的左下三角的值。
// 关键注释：matrix[i][j] = currentOddNumber; 这行代码是赋值的关键，它将当前的奇数值赋给了二维数组的相应位置。
// currentOddNumber += 2; 这行代码是生成奇数序列的关键，它将当前的奇数值加2，生成了下一个奇数。
// for (int j = 0; j <= i; j++) 这个循环是打印左下三角的关键，它只打印了数组的左下部分。
