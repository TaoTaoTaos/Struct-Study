/*
题目要求：给定一个数组nums和一个值val，需要原地移除所有数值等于val的元素，返回移除后数组的新长度。不使用额外的数组空间，必须在原地修改输入数组并在使用O(1)额外空间的条件下完成。元素的顺序可以改变，不需要考虑数组中超出新长度后面的元素。

思路：
1. 初始化两个指针i和j，都指向数组的开始位置。
2. 遍历数组，当遇到与val相等的元素时，就跳过，不进行任何操作。
3. 当遇到与val不等的元素时，就将该元素复制到数组的前部（也就是指针i所指向的位置），然后将指针i向前移动一位。
4. 这样，遍历完数组后，指针i的位置就是新的数组长度。
*/

#include <stdio.h>

int removeElement(int *nums, int numsSize, int val)
{
    int i = 0;
    for (int j = 0; j < numsSize; j++)
    {
        // 如果当前元素不等于val，就将其复制到数组的前部
        if (nums[j] != val)
        {
            nums[i] = nums[j];
            i++;
        }
    }
    // 返回新的数组长度
    return i;
}

/*
总结：
本题主要考察了数组的基本操作和双指针技巧。关键在于如何在原地修改数组，即在不使用额外空间的情况下，将与给定值不等的元素移动到数组的前部。
这里的处理方法是：使用两个指针i和j，遍历数组，当遇到与给定值不等的元素时，就将其复制到指针i所指向的位置，然后将指针i向前移动一位。
这样，遍历完数组后，指针i的位置就是新的数组长度。
*/
