/*
题目要求：将两个非递减数组a和b合并为一个升序数组（去除重复元素），并修改原数组。

思路：
1. 初始化两个指针，分别指向数组a和b的开始位置，以及一个新数组c。
2. 比较两个指针所指向的元素，将较小的元素添加到新数组c中，并将对应的指针向右移动一位。如果两个元素相等，只添加一个元素，并将两个指针都向右移动一位。
3. 当一个数组遍历完后，将另一个数组剩余的元素全部添加到新数组c中。
4. 最后，将新数组c复制回原数组a。

注意：在添加元素到新数组c时，需要检查该元素是否已经在新数组c中，以去除重复元素。
*/

#include <stdio.h>
#include <stdlib.h>

void mergeArrays(int *a, int *b, int n, int m)
{
    int *c = (int *)malloc((n + m) * sizeof(int));
    int i = 0, j = 0, k = 0;
    //! 注意i，j，k都是全局变量
    // 遍历数组a和b
    while (i < n && j < m)
    {
        // 如果a[i]小于b[j]，将a[i]添加到新数组c中，并将指针i向右移动一位
        if (a[i] < b[j])
        {
            c[k++] = a[i++];
        }
        // 如果a[i]大于b[j]，将b[j]添加到新数组c中，并将指针j向右移动一位
        else if (a[i] > b[j])
        {
            c[k++] = b[j++];
        }
        // 如果a[i]等于b[j]，将a[i]或b[j]添加到新数组c中，并将两个指针都向右移动一位
        else
        {
            c[k++] = a[i++];
            j++;
        }
    }

    // 将数组a剩余的元素添加到新数组c中
    while (i < n)
    {
        c[k++] = a[i++];
    }

    // 将数组b剩余的元素添加到新数组c中
    while (j < m)
    {
        c[k++] = b[j++];
    }

    // 将新数组c复制回原数组a
    for (i = 0; i < k; i++)
    {
        a[i] = c[i];
    }

    free(c);
}

