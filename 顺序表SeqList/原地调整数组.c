/*
题目要求：编写一个函数，将一个长度为n的数组重新排序，使得前段都是负数，中间均为0，后段均为正数。数组由负数、0、正数组成。要求时间复杂度为O(n)。

思路：
1. 初始化三个指针，left指向数组的开始位置，right指向结束位置，i从左到右遍历数组。
2. 当遇到负数时，将其交换到数组的前部（也就是指针left所指向的位置），然后将指针left和i向右移动一位。
3. 当遇到正数时，将其交换到数组的后部（也就是指针right所指向的位置），然后将指针right向左移动一位。注意此时不移动指针i，因为交换过来的数可能是负数，需要再次进行判断。
4. 当遇到0时，不进行任何操作，只将指针i向右移动一位。
5. 当指针i和right相遇时，数组就被重新排序了。
*/

#include <stdio.h>

void sortArray(int *nums, int numsSize)
{
    int left = 0, right = numsSize - 1;
    for (int i = 0; i <= right;)
    {
        if (nums[i] < 0)
        {
            // 遇到负数，交换到数组的前部
            int temp = nums[i];
            nums[i] = nums[left];
            nums[left] = temp;
            left++;
            i++;
        }
        else if (nums[i] > 0)
        {
            // 遇到正数，交换到数组的后部
            int temp = nums[i];
            nums[i] = nums[right];
            nums[right] = temp;
            right--;
        }
        else
        {
            // 遇到0，不进行任何操作
            i++;
        }
    }
}

/*
总结：
本题主要考察了数组的基本操作和双指针技巧。关键在于如何在原地修改数组，即在不使用额外空间的情况下，将负数、0、正数分别移动到数组的前部、中部和后部。
这里的处理方法是：使用三个指针，一个指向数组的开始位置，一个指向结束位置，一个从左到右遍历数组，然后根据遇到的数的正负和0进行相应的操作。
这样，当遍历完数组后，数组就被重新排序了。
*/
