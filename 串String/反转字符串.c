/*
题目要求：编写一个函数，其功能是将输入的字符串反转过来。

思路：
1. 初始化两个指针，一个指向字符串的开始位置，一个指向结束位置。
2. 然后，交换两个指针所指向的字符，并将两个指针向中间移动。
3. 当两个指针相遇时，字符串就被反转过来了。
*/

#include <string.h>

void reverseString(char *s, int sSize)
{
    int left = 0, right = sSize - 1;
    while (left < right)
    {
        // 交换两个指针所指向的字符
        char temp = s[left];
        s[left] = s[right];
        s[right] = temp;

        // 将两个指针向中间移动
        left++;
        right--;
    }
}

